const formatNumber = (countryCode, areaCode, number, format) => {
  const fullNumber = `${countryCode}${areaCode}${number}`;

  switch (format) {
    case 'formatted':
      if (countryCode === '+1') {
        return `${countryCode} (${areaCode}) ${number.substring(0, 3)}-${number.substring(3)}`;
      } else if (countryCode === '+44') {
        return `${countryCode} ${areaCode} ${number.substring(0, 4)} ${number.substring(4)}`;
      } else {
        return `${countryCode} ${areaCode} ${number}`;
      }
    case 'national':
      if (countryCode === '+1') {
        return `(${areaCode}) ${number.substring(0, 3)}-${number.substring(3)}`;
      } else {
        return `${areaCode} ${number}`;
      }
    case 'plain':
    default:
      return fullNumber.replace(/\s/g, '');
  }
};

const generateNumber = (country, numberType, format, specificAreaCode = 'random') => {
  const data = countryData[country];
  let areaCode, number, type;

  if (numberType === 'tollfree' && data.tollFree) {
    if (specificAreaCode !== 'random') {
      const found = data.tollFree.find(ac => ac.code === specificAreaCode);
      areaCode = found ? found.code : data.tollFree[randomInt(0, data.tollFree.length - 1)].code;
    } else {
      areaCode = data.tollFree[randomInt(0, data.tollFree.length - 1)].code;
    }
    number = String(randomInt(1000000, 9999999));
    type = 'tollfree';
  } else if (numberType === 'mobile' && data.mobile) {
    if (specificAreaCode !== 'random' && Array.isArray(data.mobile)) {
      const found = data.mobile.find(ac => ac.code === specificAreaCode);
      areaCode = found ? found.code : data.mobile[randomInt(0, data.mobile.length - 1)].code;
    } else if (Array.isArray(data.mobile)) {
      areaCode = data.mobile[randomInt(0, data.mobile.length - 1)].code;
    } else {
      areaCode = data.mobile[randomInt(0, data.mobile.length - 1)];
    }
    number = String(randomInt(10000000, 99999999));
    type = 'mobile';
  } else if (data.areaCodes) {
    if (specificAreaCode !== 'random') {
      const found = data.areaCodes.find(ac => ac.code === specificAreaCode);
      areaCode = found ? found.code : data.areaCodes[randomInt(0, data.areaCodes.length - 1)].code;
    } else {
      areaCode = data.areaCodes[randomInt(0, data.areaCodes.length - 1)].code;
    }
    number = String(randomInt(1000000, 9999999));
    type = numberType === 'mobile' ? 'mobile' : 'landline';
  } else {
    // For countries without specific area codes (like Norway, Denmark)
    areaCode = '';
    number = String(randomInt(10000000, 99999999));
    type = numberType;
  }

  return {
    number: formatNumber(data.code, areaCode, number, format),
    areaCode: areaCode,
    type,
  };
};

export const getAreaCodesForCountry = (country, numberType) => {
  const data = countryData[country];
  if (!data) return [];

  if (numberType === 'tollfree' && data.tollFree) {
    return data.tollFree;
  } else if (numberType === 'mobile' && data.mobile) {
    // Check if mobile is array of objects or simple array
    if (Array.isArray(data.mobile) && typeof data.mobile[0] === 'object') {
      return data.mobile;
    }
    return []; // Simple array doesn't have locations
  } else if (data.areaCodes) {
    return data.areaCodes;
  }

  return [];
};

export const generatePhoneNumbers = (country, numberType, format, count, areaCode = 'random') => {
  const results = [];
  for (let i = 0; i < count; i++) {
    results.push(generateNumber(country, numberType, format, areaCode));
  }
  return results;
};
